% --- Executes on button press in reExtractWithNewKNNclassifierADV_btn.
function reExtractWithNewKNNclassifierADV_btn_Callback(hObject, eventdata, handles)
% hObject    handle to reExtractWithNewKNNclassifierADV_btn (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)


switch get(handles.classifier_popupmenu,'Value')
    case 1
        classifier = 'built-in';
    case 2
        classifier = 'customised';
end

switch get(handles.NSMethod_popupmenu,'Value')
    case 1
        NSMethod = 'kdtree';
    case 2
        NSMethod = 'exhaustive';
end

switch get(handles.distance_popupmenu,'Value')
    case 1
        Distance = 'cityblock';
    case 2
        Distance = 'chebychev';
    case 3
        Distance = 'correlation';
    case 4
        Distance = 'cosine';
    case 5
        Distance = 'euclidean';
    case 6
        Distance = 'hamming';
    case 7
        Distance = 'jaccard';
    case 8
        Distance = 'mahalanobis';
    case 9
        Distance = 'minkowski';
    case 10
        Distance = 'seuclidean';
    case 11
        Distance = 'spearman';
    case 12
        Distance = 'distfun';
end

switch get(handles.standardise_popupmenu,'Value')
    case 1
        standardize = false;
    case 2
        standardize = true;
end

setappdata(0,'k',get(handles.k_edit,'String'));
setappdata(0,'classifier',classifier);
setappdata(0,'PVmag',get(handles.PVmag_edit,'String'));
setappdata(0,'NSMethod',NSMethod);
setappdata(0,'Distance',Distance);
setappdata(0,'Standardize',standardize);
setappdata(0,'probThr',get(handles.probThr_edit,'String'));

close (reExtractWithNewKNNclassifier_ADV);


